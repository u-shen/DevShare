import express from "express";
import dotenv from "dotenv";
import { postRouter } from "../routes/post.ts";
dotenv.config();
const app = express();
//  ╭─────────────────────────────────────────────────────────────────────────╮
//  │ REQUEST HANDLERS:                                                       │
//  ╰─────────────────────────────────────────────────────────────────────────╯
app.use(express.json());
//  ╭─────────────────────────────────────────────────────────────────────────╮
//  │ Routes Handllers:                                                       │
//  ╰─────────────────────────────────────────────────────────────────────────╯
app.use("/post", postRouter);
//  ╭─────────────────────────────────────────────────────────────────────────╮
//  │ RUN SERVER:                                                             │
//  ╰─────────────────────────────────────────────────────────────────────────╯
app.listen(process.env.PORT);
